package spaceinvaders;
public class Protector {
        int protectorX = 9;
	int protectorY = 10;
 	public Protector(int protectorX, int protectorY) {
		super();
		this.protectorX = protectorX;
		this.protectorY = protectorY;
	}
 	public int getProtectorX() {
		return protectorX;
	}
 	public void setProtectorX(int protectorX) {
		this.protectorX = protectorX;
	}
 	public int getProtectorY() {
		return protectorY;
	}
 	public void setProtectorY(int protectorY) {
		this.protectorY = protectorY;
	}
}
